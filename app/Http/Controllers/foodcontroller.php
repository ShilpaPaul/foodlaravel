    {
        $fname=request('fname');
        $forder=request('forder');
        $fprice=request('fprice');
        $fqty=request('fqty');

        $f=new foodmodel();
        $f->fname=$fname;
        $f->forder=$forder;
        $f->fprice=$fprice;
        $f->fqty=$fqty;

        $f->save();

        echo "Details saved to the database";
    }

